namespace OroIdentityServer.Services.OroIdentityServer.Api.Services;

internal sealed class IdentityRedirectService(IHttpContextAccessor context) : IIdentityRedirectService
{
    public const string StatusCookieName = "OroIdentityServer.StatusMessage";

    private static readonly CookieBuilder StatusCookieBuilder = new()
    {
        SameSite = SameSiteMode.Strict,
        HttpOnly = true,
        IsEssential = true,
        MaxAge = TimeSpan.FromSeconds(5),
    };

    [DoesNotReturn]
    public void RedirectTo(string? uri)
    {
        uri ??= "";

        // Prevent open redirects.
        if (!Uri.IsWellFormedUriString(uri, UriKind.Relative))
        {
            uri = AppContext.BaseDirectory;
        }

        // During static rendering, NavigateTo throws a NavigationException which is handled by the framework as a redirect.
        // So as long as this is called from a statically rendered Identity component, the InvalidOperationException is never thrown.
        context.HttpContext.Response.Redirect(uri);
        throw new InvalidOperationException($"{nameof(IdentityRedirectService)} can only be used during static rendering.");
    }
}
